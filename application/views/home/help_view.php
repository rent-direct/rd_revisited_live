            <h1>Tenant Tips</h1>

            <div class='list-container'>
                <ol>
                    <li>Make sure your landlord supplies you with a valid tenancy agreement signed by both you and the landlord - This helps prevent any confusion over contract length, rent payments, and who is responsible for bills etc.</li>
                    <li>Go through a comprehensive Inventory with your landlord, agreeing what is to remain in the house, and marking down any existing damage or issues. This helps resolve any disputes at the end of the contract.</li>
                    <li>Ensure that your landlord has placed your deposit in a government approved Tenancy Deposit Protection Scheme. Both you and the landlord should recieve letters from the scheme, detailing the procedures to follow at the end of the agreement in order to release the deposit back to you. This is a legal requirement.</li>
                    <li>Discover when the gas appliances were last checked for safety, and ask to see a copy of the certificate. Prefereably, take a copy to keep on file.</li>
                    <li>Ask to see the Energy Performance Certificate (EPC), if it was not already displayed on the property listing.</li>
                    <li>Ask for any record of electrical inspections</li>
                    <li>Take pride in your new home, treat it with respect, even though you are renting the property, you should always look after it as if it was your own. This helps ensure an happy tenancy.</li>
                    <li>Ask if the landlord is prepared to install smoke detectors and carbon monoxide detectors.</li>
                    <li>Always report problems promptly. You can use the built in fault reporting system on your accounts page to easily and swiftly notify your landlord of a problem.</li>
                    <li>Never lose your temper, always be polite and calm, if you treat your landlord respectively, then they will return the favour.&nbsp;</li>
                    <li>However, you should seek advice if your landlord refuses to rectify problems with the property that are their responsibility. The local authority is a good resource for professional advice, and they also have powers to force the landlord to undertake repairs if the property contains a serious health and safety hazard.</li>
                    <li>Take out contents insurance to protect your belongings from damage. This gives you piece of mind.</li>
                    <li>Always be prompt with your rent. Don&#39;t make your landlord have to chase you for money that is rightfully his. This makes for a bad tenancy relationship. If you expect to have a problem one month, contact your landlord in plenty of time. Often you will find them ammenable and relaxed, and they may even be able to provide you with a solution that helps ease the problem.</li>
                </ol>

                <h2>DON&#39;T</h2>
                <ol>
                    <li>Stop paying rent if things go wrong, if you refuse to pay your agreed rent, your landlord has the right to seek repossesion of the property on the grounds of non-payment and you could end up being evicted and accruing additional fines.</li>
                    <li>Be a nuisance. Be respectful of your neighbours. 3am parties can be viewed as intrusive and anti-social, and could lead to problems with the police, your neighbours, and even eviction.</li>
                    <li>Leave any problem to get worse. Report it in a timely fashion to your landlord</li>
                    <li>Alter, redecorate, or make structural changes to the property without prior written permission from your landlord</li>
                    <li>Sublet, rent out additional rooms in the property unless you have prior written permission from your landlord.</li>
                    <li>Conduct illegal or dangerous acts in the property.</li>
                </ol>

