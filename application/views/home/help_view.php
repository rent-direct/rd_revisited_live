                    <li>ALWAYS place the deposit in one of the government authorised Tenancy Deposit Protection Schemes. This is a legal requirement.</li>
                    <li>Ensure all gas appliances are serviced, and have a certificate from a Gas Safe registered engineer. This is a legal requirement.</li>
